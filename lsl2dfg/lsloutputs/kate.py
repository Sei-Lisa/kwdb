import sys

# Python3 does not have this function, so this replaces it with equivalent behavior.
def cmp(a, b):
  return (a > b) - (a < b)

def output(document, defaultdescs, databaseversion, infilename, outfilename, lang, tag):

  version = "0.0.20130817000"

  keywords = []
  types = []
  constants = []
  functions = []
  events = []

  for element in document:
    if element["cat"] == "keyword":
      keywords.append(element["name"])
    elif element["cat"] == "type":
      types.append(element["name"])
    elif element["cat"] == "constant":
      constants.append(element["name"])
    elif element["cat"] == "function":
      functions.append(element["name"])
    elif element["cat"] == "event":
      events.append(element["name"])

  keywords.sort()
  types.sort()
  constants.sort()
  try:
    functions.sort(key=lambda x: x.lower())
  except:
    functions.sort(lambda x,y: cmp(x.lower(), y.lower()))
  events.sort()

  if infilename is not None:
    inf = open(infilename, "r")
  else:
    inf = sys.stdin

  try:
    inputlines = inf.readlines()

  finally:
    if infilename is not None:
      inf.close()

  if outfilename is not None:
    outf = open(outfilename, "w")
  else:
    outf = sys.stdout

  try:

    for line in inputlines:
      if not line.startswith("<<< %s KEYWORDS >>>" % tag):
        outf.write(line)
      else:
        outf.write('<!-- Generated by LSL2 Derived Files Generator. Database version: %s; output module version: %s -->\n'
          % (databaseversion, version))

        outf.write('\t\t<list name="keywords">\n')
        for element in keywords:
          outf.write('\t\t\t<item> ' + element + ' </item>\n')
        outf.write('\t\t</list>\n')

        outf.write('\t\t<list name="types">\n')
        for element in types:
          outf.write('\t\t\t<item> ' + element + ' </item>\n')
        outf.write('\t\t</list>\n')

        outf.write('\t\t<list name="functions">\n')
        for element in functions:
          outf.write('\t\t\t<item> ' + element + ' </item>\n')
        outf.write('\t\t</list>\n')

        outf.write('\t\t<list name="constants">\n')
        for element in constants:
          outf.write('\t\t\t<item> ' + element + ' </item>\n')
        outf.write('\t\t</list>\n')

        outf.write('\t\t<list name="events">\n')
        for element in events:
          outf.write('\t\t\t<item> ' + element + ' </item>\n')
        outf.write('\t\t</list>\n')

  finally:
    if outfilename is not None:
      outf.close()


pass
